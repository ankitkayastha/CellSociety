
public class Cell {
	private int currentState;
	private int nextState;
	
	
	public int getCurrentState() {
		return currentState;
	}
	public void setCurrentState(int state) {
		currentState = state;
	}
	public int getNextState() {
		return nextState;
	}
	public void setNextState(int state) {
		nextState = state;
	}
}
